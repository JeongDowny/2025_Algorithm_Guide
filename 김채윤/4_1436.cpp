#include <iostream>

using namespace std;

int main() {
    int N;
    cin >> N;

    int num = 666;
    int ans = num;
    string num666 = "666";

    while (N - 1) {
        num += 1;
        if (to_string(num).find(num666) != string::npos) {
            N -= 1;
            ans = num;
        }
    }
    cout << num;

    return 0;
}

